public class user {
    int id;
    String name;

    public user() {
        super();
    }

    @Override
    public boolean equals(Object obj) {
        return super.equals(obj);
    }

    public user(String name, int id) {
        this.name = name;
        this.id = id;
    }

    @Override
    public String toString() {
        return super.toString();
    }
}
